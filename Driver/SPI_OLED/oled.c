/********************************************************************
*	@func: 	oled_write_byte
*	@brief: 发送一个字节
*	@param: uint8_t spi_byte：	待发送的字节
*	@rtvl:  NONE
*	@date： 20220918
************************************************************************/
static void oled_write_byte(uint8_t write_byte)
{
	spi_master_send_recv_byte(write_byte);	//底层硬件SPI
}

/********************************************************************
*	@func: 	oled_write_operate
*	@brief: 根据命令模式写入一个字节的数据
*	@param: mode：工作模式：OLED_CMD：指令模式，OLED_DATA：数据模式
*			dat: 写入的一个字节
*	@rtvl:  NONE
*	@date： 20220918
************************************************************************/
static void oled_write_operate(OLED_WORK_MODE mode, u8 dat)
{
	/*片选拉低*/
	OLED_CS = 0;
	
	/*根据模式写入数据或命令*/
	if (mode == OLED_DATA){	//写入数据
		OLED_DC = 1;
	}else if(mode == OLED_CMD){ 	//写入命令
		OLED_DC = 0;
	}	
	oled_write_byte(dat);
	/*片选拉高*/
	OLED_CS = 1;
}



//---------------------------------------------------------------------------------------------------------------------------------------------
//	函 数 名: oled_set_pos
//	功能说明: oled 显示坐标设置
//	形    参: x:横坐标;y：纵坐标
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 无
//	作    者: by 霁风AI
//---------------------------------------------------------------------------------------------------------------------------------------------
void oled_set_pos(uint8_t x, uint8_t y)
{
	oled_write_operate(OLED_CMD, 0xb0 + y);	//设置页(page)地址
	oled_write_operate(OLED_CMD, ((x & 0xf0) >> 4) | 0x10);	//列高四位地址
	oled_write_operate(OLED_CMD, (x & 0x0f) | 0x01);	//列低四位地址
}

//---------------------------------------------------------------------------------------------------------------------------------------------
//	函 数 名: oled_dis_on
//	功能说明: oled 打开显示
//	形    参: 无
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 写入AF指令打开
//	作    者: by 霁风AI
