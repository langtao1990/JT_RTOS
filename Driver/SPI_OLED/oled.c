void oled_dis_off(void)
{
	oled_write_operate(OLED_CMD, 0x8d);//设置DCDC
	oled_write_operate(OLED_CMD, 0x10);//关闭DCDC
	oled_write_operate(OLED_CMD, 0xae);//关闭OLED
}

//---------------------------------------------------------------------------------------------------------------------------------------------
//	函 数 名: oled_dis_clear
//	功能说明: oled 清除显示
//	形    参: 无
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 写入00清除
//	作    者: by 霁风AI
//---------------------------------------------------------------------------------------------------------------------------------------------
void oled_dis_clear(void)
{
	uint8_t page = 0;
	uint8_t i = 0;
	
	for (page = 0; page < 8; page++)
	{
		oled_write_operate(OLED_CMD, 0xb0 + page);	//设置页地址(0--7)
		oled_write_operate(OLED_CMD, 0x00);	//设置显示位置―列低地址
		oled_write_operate(OLED_CMD, 0x10); 	//设置显示位置―列高地址
	
		for(i = 0; i < 128; i++)
		{
			oled_write_operate(OLED_DATA, 0x00);	//0x00清屏
		}
	}
}

//---------------------------------------------------------------------------------------------------------------------------------------------
//	函 数 名: oled_dis_set
//	功能说明: oled 显示出可视区域
//	形    参: 无
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 写入ff设置
//	作    者: by 霁风AI
