//	函 数 名: oled_dis_picture
//	功能说明: oled 数字
//	形    参: x：横坐标(列);y:纵坐标(行);num：显示数字;len：数字长度;size_num：数字大小
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 无
//	作    者: by 霁风AI
//---------------------------------------------------------------------------------------------------------------------------------------------
void oled_dis_num(uint8_t x, uint8_t y, uint32_t num, uint8_t len, uint8_t size_num)
{
	uint8_t i = 0;
	uint8_t temp = 0;
	uint8_t enshow = 0;
	
	for (i = 0; i < len; i++)
	{
		temp = (num / (oled_pow(10,len-i-1))) % 10;//把显示的数字一位一位取出来
		if((enshow == 0) && (i < (len-1)))
		{
			if(temp == 0)
			{
				oled_dis_one_char(x + (size_num / 2) * i, y, ' ');
				continue;
			}
			else
			{
				enshow = 1;
			}
		}
		oled_dis_one_char(x + (size_num / 2) * i, y, temp + '0');
	}
}



//---------------------------------------------------------------------------------------------------------------------------------------------
//	函 数 名: oled_init
//	功能说明: oled 初始化
//	形    参: 无
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 外部调用初始化OLED
//	作    者: by 霁风AI
//---------------------------------------------------------------------------------------------------------------------------------------------
void oled_init(void)
{
	spi_master_init();
	
	OLED_RES = 1;
	timer_delay_us(1000*200);
	OLED_RES = 0;
	timer_delay_us(1000*200);
	OLED_RES = 1;
		
	oled_write_operate(OLED_CMD, 0xAE);	//关闭OLED
	oled_write_operate(OLED_CMD, 0x00);//设置列低位地址
	oled_write_operate(OLED_CMD, 0x10);//设置列高位地址
	oled_write_operate(OLED_CMD, 0x40);	//设置起始行地址及映射RAM显示起始行 (0x00~0x3F)
	oled_write_operate(OLED_CMD, 0x81);	//对比度设置
	oled_write_operate(OLED_CMD, 0xCF); 	// Set SEG Output Current Brightness
	oled_write_operate(OLED_CMD, 0xA1);	//--Set SEG/Column Mapping     0xa0左右反置 0xa1正常
	oled_write_operate(OLED_CMD, 0xC8);	//Set COM/Row Scan Direction   0xc0上下反置 0xc8正常
	oled_write_operate(OLED_CMD, 0xA8);	//设置驱动路数(1 to 64)
	oled_write_operate(OLED_CMD, 0x3f);	//--1/64 duty
	oled_write_operate(OLED_CMD, 0xD3);	//-设置显示偏移(0x00~0x3F)
	oled_write_operate(OLED_CMD, 0x00);	//-not offset
	oled_write_operate(OLED_CMD, 0xd5);	//--set display clock divide ratio/oscillator frequency
	oled_write_operate(OLED_CMD, 0x80);	//--set divide ratio, Set Clock as 100 Frames/Sec
	oled_write_operate(OLED_CMD, 0xD9);	//--set pre-charge period
	oled_write_operate(OLED_CMD, 0xF1);	//Set Pre-Charge as 15 Clocks & Discharge as 1 Clock
	oled_write_operate(OLED_CMD, 0xDA);	//--set com pins hardware configuration
	oled_write_operate(OLED_CMD, 0x12);
	oled_write_operate(OLED_CMD, 0xDB);	//--set vcomh
	oled_write_operate(OLED_CMD, 0x40);	//Set VCOM Deselect Level
	oled_write_operate(OLED_CMD, 0x20);	//设置页地址模式(0x00/0x01/0x02)
	oled_write_operate(OLED_CMD, 0x02);	//
	oled_write_operate(OLED_CMD, 0x8D);	//--set Charge Pump enable/disable
	oled_write_operate(OLED_CMD, 0x14);	//--set(0x10) disable
	oled_write_operate(OLED_CMD, 0xA4);	//显示开启(显示:A4;无显示:A5)
	oled_write_operate(OLED_CMD, 0xA7);	// 背景正反向显示 (0xa6:正显;a7:反显) 
	oled_write_operate(OLED_CMD, 0xAF);	//打开显示
	
	oled_dis_clear();
	oled_set_pos(0, 0); 	
}  

