//---------------------------------------------------------------------------------------------------------------------------------------------
//	函 数 名: oled_pow
//	功能说明: 计算 m的n次方
//	形    参: m：底数;n：指数
//	返 回 值: 无
//	日    期: 2020-04-11
//  备    注: 无
//	作    者: by 霁风AI
//---------------------------------------------------------------------------------------------------------------------------------------------
uint32_t oled_pow(uint8_t m, uint8_t n)
{
	uint32_t ret = 1;
	
	while(n--)
	{
		ret *= m;
	}
	
	return ret;
}

